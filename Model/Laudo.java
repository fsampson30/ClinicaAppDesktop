package Model;

public class Laudo {
    
    private String dataAdmissao;
    private String caminhoFoto;
    private String caminhoLaudo;
    private String codigoExame;
    private String cpf;
    private String matricula;

    public String getDataAdmissao() {
        return dataAdmissao;
    }

    public void setDataAdmissao(String dataAdmissao) {
        this.dataAdmissao = dataAdmissao;
    }

    public String getCaminhoFoto() {
        return caminhoFoto;
    }

    public void setCaminhoFoto(String caminhoFoto) {
        this.caminhoFoto = caminhoFoto;
    }

    public String getCaminhoLaudo() {
        return caminhoLaudo;
    }

    public void setCaminhoLaudo(String caminhoLaudo) {
        this.caminhoLaudo = caminhoLaudo;
    }

    public String getCodigoExame() {
        return codigoExame;
    }

    public void setCodigoExame(String codigoExame) {
        this.codigoExame = codigoExame;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getMatricula() {
        return matricula;
    }

    public void setMatricula(String matricula) {
        this.matricula = matricula;
    }
    
    
    
}
